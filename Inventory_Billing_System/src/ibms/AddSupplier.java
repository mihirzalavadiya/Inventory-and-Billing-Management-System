    private void companytableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_companytableMouseClicked
        try
        {
            btncompany_save.setEnabled(false);
            btncompany_update.setEnabled(true);
           
            int r= companytable.getSelectedRow();
            int c=companytable.getSelectedColumn();
            Object s=companytable.getValueAt(r, 0);
            String NameVal = s.toString();
            ResultSet rs = st.executeQuery("select * from Supplier where Company_Name='"+NameVal+"'");
            rs.next();
            lblcompanyid.setText(""+rs.getInt("Supplier_ID"));
            txtcompaney_name.setText(""+rs.getString("Company_Name"));
            txtcompany_address.setText(""+rs.getString("Address"));
            txtcontact_number.setText(""+rs.getString("Phone_Number"));
            txtcompany_email.setText(""+rs.getString("Email"));
            txtcontact_person_name.setText(""+rs.getString("Contact_Person_Name"));
            txtcompany_gstno.setText(""+rs.getString("Gst_No"));
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_companytableMouseClicked

