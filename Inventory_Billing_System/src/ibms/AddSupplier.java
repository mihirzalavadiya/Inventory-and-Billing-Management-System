    private void initJtable()
    {
        try
        {
            conn = DriverManager.getConnection("jdbc:ucanaccess://G:\\Mydb.accdb");
            st = conn.createStatement();
            
            header = new Vector<String>();
            header.add("Company Name");
            header.add("Contact Person Name");
            header.add("Contact No.");
            header.add("Email");
            header.add("Address");
            header.add("GST No.");
            
            ResultSet rs = st.executeQuery("SELECT * FROM Supplier ORDER BY Company_Name");
            data = new Vector<Vector<String>>();
            while(rs.next())
            {
                Vector<String> custVet = new Vector<String>();
                custVet.add(rs.getString("Company_Name"));
                custVet.add(rs.getString("Contact_Person_Name"));
                custVet.add(rs.getString("Phone_Number"));
                custVet.add(rs.getString("Email"));
                custVet.add(rs.getString("Address"));
                custVet.add(rs.getString("Gst_No"));
                data.add(custVet);
            }
           DefaultTableModel dtm = new DefaultTableModel(data,header);
           companytable.setModel(dtm);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }
