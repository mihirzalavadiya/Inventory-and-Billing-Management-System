    private void save()
    {
        try 
        {   
            n1 = txtcname.getText();
            m1 = txtcmobile.getText();
            a1 = txtaddress.getText();
            e1 = txtemail.getText();
            if(n1.isEmpty()||m1.isEmpty()||a1.isEmpty()||e1.isEmpty())
            {
                JOptionPane.showMessageDialog(null,"Please Enter above fild");
            }
            else if(!n1.isEmpty() && !m1.isEmpty() && !a1.isEmpty() && !e1.isEmpty())
            {
                
                conn = DriverManager.getConnection("jdbc:ucanaccess://G:\\Mydb.accdb");
                
                //pst = conn.prepareStatement("INSERT into Customer(FullName,Mobile_No,Address,Email) VALUES('"+n1+"','"+m1+"','"+a1+"','"+e1+"')");
                pst = conn.prepareStatement("INSERT into Customer(Customer_Name,Mobile_No,Address,Email,Created_Date_Time,Updated_Date_Time) VALUES(?,?,?,?,?,?)");
                pst.setString(1, n1);
                pst.setString(2, m1);
                pst.setString(3, a1);
                pst.setString(4, e1);
                pst.setDate(5, new Date(cal.getTimeInMillis()));
                pst.setDate(6, new Date(cal.getTimeInMillis()));
                pst.executeUpdate();
                
                conn.commit();
                conn.close();                
                
                JOptionPane.showMessageDialog(null,"Successfully Inserted");

                initJtable();
                
                txtcname.setText("");
                txtcmobile.setText("");
                txtaddress.setText("");
                txtemail.setText("");
                
            }
        } 
        catch (SQLException ex) 
        {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
