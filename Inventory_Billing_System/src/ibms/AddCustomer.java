    private void initJtable()
    {
        try
        {
            conn = DriverManager.getConnection("jdbc:ucanaccess://G:\\Mydb.accdb");
            st = conn.createStatement();
            
            header = new Vector<String>();
            header.add("Customer Name");
            header.add("Mobile Number");
            header.add("Address");
            header.add("Email");
            
            ResultSet rs = st.executeQuery("SELECT * FROM Customer ORDER BY Customer_Name");
            data = new Vector<Vector<String>>();
            while(rs.next())
            {
                Vector<String> custVet = new Vector<String>();
                custVet.add(rs.getString("Customer_Name"));
                custVet.add(rs.getString("Mobile_No"));
                custVet.add(rs.getString("Address"));
                custVet.add(rs.getString("Email"));
                data.add(custVet);
            }
           DefaultTableModel dtm = new DefaultTableModel(data,header);
           jTable1.setModel(dtm);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }
